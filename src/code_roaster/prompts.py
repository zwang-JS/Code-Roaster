def get_persona(persona_name: str) -> dict:
    """
    获取指定名称的性格配置。

    Args:
        persona_name: 性格名称 (toxic, professor, coworker, kfc)

    Returns:
        dict: 包含 name, emoji, description, system_prompt,
              reflection_prompt, no_diff_message 的配置字典
    """
    persona = PERSONAS.get(persona_name)
    if persona is None:
        # 默认返回 toxic
        return PERSONAS["toxic"]
    return persona


def get_available_personas() -> list:
    """
    获取所有可用性格的名称列表。

    Returns:
        list: 性格名称列表
    """
    return list(PERSONAS.keys())
